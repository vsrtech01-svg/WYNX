export const getCities = (state) => statesAndCities[state] || [];

export const lookupPincode = (pincode) => {
  const pin = parseInt(pincode, 10);
  if (isNaN(pin) || pincode.length !== 6) return null;
  
  for (const entry of pincodeMapping) {
    if (pin >= entry.range[0] && pin <= entry.range[1]) {
      return { state: entry.state, city: entry.city };
    }
  }
  return null;
};

